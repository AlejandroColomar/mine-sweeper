void	print_verbose		(int verbose, void *print_func, int arg)
{

	if (arg != -1) {
		if (flag_V >= verbose) {
			(*(void (*)(bool)) print_func)(arg);
		}
	} else {
		if (flag_V >= verbose) {
			(*(void (*)()) print_func)();
		}
	}
}

